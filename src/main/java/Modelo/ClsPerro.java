
package Modelo;

/**
 *
 * @author diego
 */
public class ClsPerro {
    public String nombre;
    public String age;

    public ClsPerro(String nombre) {
        this.nombre = nombre;
    }

    
    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }
    
    
}
